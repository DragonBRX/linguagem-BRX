# ============================================================
# font8_init
# ============================================================
# Inicializa o subsistema de fonte.
# Na pratica, so marca que a fonte esta pronta (os dados
# ja estao na secao .rodata).
# ============================================================
font8_init:
    xorl %eax, %eax
    retq

# ============================================================
# font8_get_glyph
# ============================================================
# Retorna ponteiro para 8 bytes de glifo.
#
# Entrada:
#   %rdi = ponteiro para o byte do caractere na string
#
# Saida:
#   %rax = ponteiro para 8 bytes do glifo (na .rodata)
#   %rcx = numero de bytes consumidos da string (1 ou 2)
#
# Preserva: %rbx, %rbp, %r12-%r15 (caller-saved: destruidos)
# ============================================================
font8_get_glyph:
    movzbl (%rdi), %eax          # byte 0

    # Caractere ASCII (0x00-0x7F)
    testb $0x80, %al
    jnz .Lutf8_check

    # Dentro do range ASCII imprimivel (0x20-0x7E)?
    cmpl $0x20, %eax
    jb .Lunknown_char
    cmpl $0x7E, %eax
    ja .Lunknown_char

    # Indice = char - 0x20, cada glifo tem 8 bytes
    subl $0x20, %eax
    imulq $8, %rax, %rax
    leaq font8_glyphs(%rax), %rax
    movq $1, %rcx              # consumiu 1 byte
    retq

.Lutf8_check:
    # Sequencia UTF-8 de 2 bytes (0xC0-0xDF como lead byte)
    cmpb $0xC3, %al
    jne .Lunknown_char

    # Le o segundo byte
    movzbl 1(%rdi), %ecx
    # Verifica se e continuation byte (10xxxxxx)
    andb $0xC0, %cl
    cmpb $0x80, %cl
    jne .Lunknown_char

    # Re-le o segundo byte limpo (sem mascara)
    movzbl 1(%rdi), %ecx

    # Lookup: offset = byte2 - 0x80
    subb $0x80, %cl
    movzbl %cl, %ecx

    # Le entrada da tabela de lookup
    movzbl font8_utf8_lookup(%rcx), %eax

    # 0xFF = nao encontrado
    cmpb $0xFF, %al
    je .Lunknown_char

    # eax = indice do glifo, cada um tem 8 bytes
    imulq $8, %rax, %rax
    leaq font8_glyphs(%rax), %rax
    movq $2, %rcx              # consumiu 2 bytes
    retq

.Lunknown_char:
    leaq unknown_glyph, %rax
    movq $1, %rcx              # consumiu 1 byte
    retq
